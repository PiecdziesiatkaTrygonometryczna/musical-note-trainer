document.getElementById("stringECheckboxToggle").addEventListener("change", function() {
    var stringImage = document.getElementById("1st-string-wholes");
    if (this.checked) {
        stringImage.style.display = "inline";
    } else {
        stringImage.style.display = "none";
    }
});

